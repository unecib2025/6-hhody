#8) Сортировка с помощью sort() может быть полезна 
#   при анализе событий безопасности, потому что она упорядочивает данные
#   и делает их более понятными. Благодаря сортировке легче замечать аномалии
#   и подозрительные действия, а также проще находить повторяющиеся или редкие события.
#   Кроме того, сортировка помогает правильно анализировать события по времени и быстрее
