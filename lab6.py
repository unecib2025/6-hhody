#КОНТРОЛЬНЫЕ ВОПРОСЫ

#1) "append(элемент)" добавляет элемент в конец списка. "insert(позиция, элемент)"
#   добавляет элемент на конкретную позицию в списке.
#   Например:
a=[1, 2, 3, 4]
a.append(2)
print(a)
#   Результат:
[1, 2, 3, 4, 2]

a=[1, 2, 3, 4]
a.insert(0, 2)
print(a)
#   Результат:
[2, 1, 2, 3, 4]

#2) Удаляет последний элемент и возвращает его.
#   Например:
a = [10, 20, 30]
x = a.pop() #удалит 30
print(x)
#   Результат:
30

#3) Нельзя, "remove()" удаляет только первый найденный элемент в списке.
#   Но есть способ.
#   Например:
a=["error", "ok", "ok", "fail", "ok"]
while "ok" in a:
    a.remove("ok")
print(a)
#   Результат:
["error", "fail"]

#4) Метод "reverse()" инвертирует порядок следования значений в списке,
#   то есть меняет его на противоположный.
#   Например:
names=['Gvido','Roman','Timur'] 
names.reverse() 
print(names)
#   Результат:
['Timur', 'Roman', 'Gvido']

#5) Метод "count()" считает, сколько раз элемент встречается в списке.
